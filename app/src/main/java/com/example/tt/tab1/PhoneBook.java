package com.example.tt.tab1;


class PhoneBook {

    private String id;
    private String name;
    private String  tel;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    public String  getTel() {
        return tel;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

}